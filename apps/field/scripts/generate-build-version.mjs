#!/usr/bin/env node
/**
 * Writes a unique semver-compatible version for each dev server start / production build.
 * Format: {packageVersion}+{gitSha|nogit}.{UTC timestamp}
 * Example: 0.0.0+a1b2c3d.20250614.230912
 */
import { execSync } from 'node:child_process';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const fieldRoot = join(scriptDir, '..');
const pkg = JSON.parse(readFileSync(join(fieldRoot, 'package.json'), 'utf8'));
const mode = process.argv.includes('--dev') ? 'dev' : 'build';

function getGitSha() {
  const fromEnv = process.env.MMAP_BUILD_SHA?.trim();
  if (fromEnv) {
    return fromEnv;
  }

  try {
    return execSync('git rev-parse --short HEAD', {
      cwd: join(fieldRoot, '../..'),
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).trim();
  } catch {
    return 'nogit';
  }
}

function getUtcStamp() {
  const now = new Date();
  const pad = (value) => String(value).padStart(2, '0');
  return [
    now.getUTCFullYear(),
    pad(now.getUTCMonth() + 1),
    pad(now.getUTCDate()),
    '.',
    pad(now.getUTCHours()),
    pad(now.getUTCMinutes()),
    pad(now.getUTCSeconds()),
  ].join('');
}

const baseVersion = /^\d+\.\d+\.\d+$/.test(String(pkg.version)) ? String(pkg.version) : '0.0.0';
const buildLabel = mode === 'dev' ? 'dev' : getGitSha();
const buildMetadata = `${buildLabel}.${getUtcStamp()}`;
const version = `${baseVersion}+${buildMetadata}`;

const outDir = join(fieldRoot, 'src/generated');
const outFile = join(outDir, 'build-version.ts');

mkdirSync(outDir, { recursive: true });
writeFileSync(
  outFile,
  `/** Generated by scripts/generate-build-version.mjs - do not edit */\nexport const APP_VERSION = '${version}';\n`,
  'utf8',
);

const publicDir = join(fieldRoot, 'public');
mkdirSync(publicDir, { recursive: true });
writeFileSync(join(publicDir, 'version.json'), `${JSON.stringify({ version }, null, 2)}\n`, 'utf8');

console.log(`@mmap/field version: ${version}`);
